class Employee
{
    public string name;
    public string Name
    {
        get
        {
            return this.name;
        }
        set
        {
            name = value;
        }
    }
    public string surname;
    public string Surname
    {
        get
        {
            return this.surname;
        }
        set
        {
            surname = value;
        }
    }
}